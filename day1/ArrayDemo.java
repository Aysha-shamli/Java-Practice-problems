public class ArrayDemo {
    public static void main(String[] args) {
        int[] values = {1,2,3,4,5};
        for (int val : values){
            System.out.println(val);
        }
        System.out.println(values);
        System.out.println(values.length);
        System.out.println(values[2]);

    }
}
